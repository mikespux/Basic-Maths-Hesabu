package com.wachi.hesabu.model;

public class ColorModel {

    public int drawable;
    public int color, DarkColor, cell;

    public ColorModel(int drawable, int color, int DarkColor, int cell) {
        this.drawable = drawable;
        this.color = color;
        this.DarkColor = DarkColor;
        this.cell = cell;
    }
}
